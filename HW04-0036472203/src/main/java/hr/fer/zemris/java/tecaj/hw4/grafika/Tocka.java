/**
 * Razred koji predstavlja točku u 2D prostoru.
 * 
 * @author Petra Rebernjak - 0036472203
 *
 */
public class Tocka {

	/**
	 * x koorinara točke.
	 */
	int x;

	/**
	 * y koordinata točke.
	 */
	int y;

	/**
	 * Konstruktor. Inicijalizira x i y koordinatu točke.
	 * 
	 * @param x
	 *            x koordinata točke.
	 * @param y
	 *            y koordinata točke.
	 */
	public Tocka(int x, int y) {
		this.x = x;
		this.y = y;
	}

}
